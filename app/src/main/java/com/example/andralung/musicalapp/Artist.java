package com.example.andralung.musicalapp;

class Artist {

    private String myArtist;
    private int myIcon;

    public Artist (String myArtist, int myIcon){

        this.myArtist = myArtist;
        this.myIcon = myIcon;
    }


    public String getMyArtist() {
        return myArtist;
    }

    public int getMyIcon() {
        return myIcon;
    }


}
